/**
 * Icon component with consistent sizing and className support.
 * 
 * @param {string} name - The icon name from the Icons object
 * @param {number} size - Size in pixels (default: 16)
 * @param {string} className - Optional CSS className
 */
export function Icon({ name, size = 16, className = "" }) {
    const icon = Icons[name];
    if (!icon) {
        console.warn(`Icon "${name}" not found`);
        return null;
    }

    return (
        <span
            className={className}
            style={{
                display: "inline-flex",
                alignItems: "center",
                justifyContent: "center",
                width: size,
                height: size,
            }}
        >
            {icon}
        </span>
    );
}

